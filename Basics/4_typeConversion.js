// Type Conversion

// JavaScript automatically converts between data types when necessary (type coercion), but you can also manually convert types.

// Implicit Conversion:

let result = "5" + 10; // "510" (number converted to string)
console.log(result);



// Explicit Conversion:

let strNum = "123";
let num = Number(strNum); // Converts string to number
console.log(num);
